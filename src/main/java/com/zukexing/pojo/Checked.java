package com.zukexing.pojo;


public class Checked {

  private Integer checkId;
  private String checkTitle;
  private String checkCont;
  private Integer adminId;
  private Integer houseId;
  private String checkTime;
  private String checkRes;


  public Integer getCheckId() {
    return checkId;
  }

  public void setCheckId(Integer checkId) {
    this.checkId = checkId;
  }


  public String getCheckTitle() {
    return checkTitle;
  }

  public void setCheckTitle(String checkTitle) {
    this.checkTitle = checkTitle;
  }


  public String getCheckCont() {
    return checkCont;
  }

  public void setCheckCont(String checkCont) {
    this.checkCont = checkCont;
  }


  public Integer getAdminId() {
    return adminId;
  }

  public void setAdminId(Integer adminId) {
    this.adminId = adminId;
  }


  public Integer getHouseId() {
    return houseId;
  }

  public void setHouseId(Integer houseId) {
    this.houseId = houseId;
  }


  public String getCheckTime() {
    return checkTime;
  }

  public void setCheckTime(String checkTime) {
    this.checkTime = checkTime;
  }


  public String getCheckRes() {
    return checkRes;
  }

  public void setCheckRes(String checkRes) {
    this.checkRes = checkRes;
  }

}
